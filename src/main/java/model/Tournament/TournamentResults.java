package model.Tournament;

public class TournamentResults {

    private String d_MapName;
    private int d_GameNumber;
    private String d_GameWinner;

    public String getD_MapName() {
        return d_MapName;
    }

    public void setD_MapName(String d_MapName) {
        this.d_MapName = d_MapName;
    }

    public int getD_GameNumber() {
        return d_GameNumber;
    }

    public void setD_GameNumber(int d_GameNumber) {
        this.d_GameNumber = d_GameNumber;
    }

    public String getD_GameWinner() {
        return d_GameWinner;
    }

    public void setD_GameWinner(String d_GameWinner) {
        this.d_GameWinner = d_GameWinner;
    }
}
